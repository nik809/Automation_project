package Java_control_statement;

public class class_001 {

	public static void main(String[] args) {

		String s = "ef";
		switch (s) {
		case "cd":
			System.out.println("value is cd");
			break;
		case"ef":
			System.out.println("value is ef");
         break;
		case"ab":
		System.out.println("value is ab");
		break;
		
		default:
			System.out.println("all conditions is true");
		}
		
		
	}
}	
		
		

